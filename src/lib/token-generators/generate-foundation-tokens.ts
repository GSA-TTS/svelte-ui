/**
 * Generate Foundation Layer Tokens
 * 
 * This generator creates the foundation layer CSS files that contain
 * design system tokens as CSS custom properties.
 * 
 * Supports: USWDS (default), Material Design, Bootstrap
 * 
 * Architecture:
 *   Design System → Token Parser → Foundation Tokens → Semantic Tokens → Component Tokens
 * 
 * Usage:
 *   npm run generate:foundation           # Defaults to USWDS
 *   tsx src/lib/token-generators/generate-foundation-tokens.ts uswds
 *   tsx src/lib/token-generators/generate-foundation-tokens.ts material
 */

import { writeFileSync, mkdirSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import type { ParsedTokens } from './types.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

/**
 * Supported design systems
 */
export type DesignSystem = 'uswds' | 'material' | 'bootstrap';

/**
 * Load parser for the specified design system
 * 
 * @param designSystem - Design system to load parser for
 * @returns Parsed tokens in standardized format
 */
async function loadParser(designSystem: DesignSystem): Promise<ParsedTokens> {
  switch (designSystem) {
    case 'uswds': {
      const { parseUSWDSTokens } = await import('./parse-tokens/uswds/index.js');
      return parseUSWDSTokens();
    }
    
    case 'material':
      throw new Error(
        'Material Design token parser not yet implemented.\n\n' +
        'To add Material Design support:\n' +
        '  1. Create parse-tokens/material/index.ts\n' +
        '  2. Implement parseMaterialTokens() function\n' +
        '  3. Export parseMaterialTokens from the module\n\n' +
        'Then run: npm run parse:tokens material'
      );
    
    case 'bootstrap':
      throw new Error(
        'Bootstrap token parser not yet implemented.\n\n' +
        'To add Bootstrap support:\n' +
        '  1. Create parse-tokens/bootstrap/index.ts\n' +
        '  2. Implement parseBootstrapTokens() function\n' +
        '  3. Export parseBootstrapTokens from the module\n\n' +
        'Then run: npm run parse:tokens bootstrap'
      );
    
    default:
      throw new Error(`Unsupported design system: ${designSystem}`);
  }
}

/**
 * Generate color tokens CSS
 */
function generateColorCSS(tokens: ParsedTokens, designSystem: string): string {
  const designSystemUpper = designSystem.toUpperCase();
  const lines: string[] = [
    '/**',
    ` * ${designSystemUpper} Foundation Color Tokens`,
    ' * ',
    ` * System colors, theme colors, and state colors from ${designSystemUpper}`,
    ` * Generated from ${designSystemUpper} v${tokens.version}`,
    ' * ',
    ' * Do not edit this file directly. It is generated by:',
    ' * npm run generate:foundation',
    ' */',
    '',
    ':root {',
    '  /* ================================== */',
    '  /* System Colors                      */',
    '  /* ================================== */',
    ''
  ];

  // System colors
  Object.entries(tokens.colors.system).forEach(([name, token]) => {
    lines.push(`  --${name}: ${token.value};`);
  });

  lines.push('');
  lines.push('  /* ================================== */');
  lines.push('  /* Theme Colors                       */');
  lines.push('  /* ================================== */');
  lines.push('');

  // Theme colors
  Object.entries(tokens.colors.theme).forEach(([name, token]) => {
    const value = token.reference ? `var(--${token.value})` : token.value;
    lines.push(`  --${name}: ${value};`);
  });

  lines.push('');
  lines.push('  /* ================================== */');
  lines.push('  /* State Colors                       */');
  lines.push('  /* ================================== */');
  lines.push('');

  // State colors
  Object.entries(tokens.colors.state).forEach(([name, token]) => {
    const value = token.reference ? `var(--${token.value})` : token.value;
    lines.push(`  --${name}: ${value};`);
  });

  lines.push('}');
  lines.push('');

  return lines.join('\n');
}

