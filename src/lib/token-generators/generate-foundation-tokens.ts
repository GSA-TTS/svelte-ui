/**
 * Main generator function
 */
export async function generateFoundationTokens(designSystem: DesignSystem = 'uswds') {
  const designSystemUpper = designSystem.toUpperCase();
  console.log(`=== ${designSystemUpper} Foundation Token Generator ===\n`);

  // Parse tokens via dynamic import
  console.log(`Parsing ${designSystemUpper} tokens...`);
  const tokens = await loadParser(designSystem);
  console.log(`  Parsed ${Object.keys(tokens.colors.system).length + Object.keys(tokens.colors.theme).length + Object.keys(tokens.colors.state).length} color tokens`);

  // Create output directory
  const OUTPUT_DIR = resolve(__dirname, `../../tokens/foundation/${designSystem}`);
  mkdirSync(OUTPUT_DIR, { recursive: true });
  console.log(`\nOutput directory: ${OUTPUT_DIR}`);

  // Generate token CSS files
  console.log('\nGenerating token CSS files...');

  const files = [
    { name: 'color.css', content: generateColorCSS(tokens, designSystem) },
    { name: 'spacing.css', content: generateSpacingCSS(tokens, designSystem) },
    { name: 'typesetting.css', content: generateTypesettingCSS(tokens, designSystem) },
    { name: 'shadow.css', content: generateShadowCSS(tokens, designSystem) },
    { name: 'opacity.css', content: generateOpacityCSS(tokens, designSystem) },
    { name: 'z-index.css', content: generateZIndexCSS(tokens, designSystem) },
    { name: 'flex.css', content: generateFlexCSS(tokens, designSystem) },
    { name: 'order.css', content: generateOrderCSS(tokens, designSystem) },
    { name: 'all.css', content: generateAllCSS(tokens, designSystem) }
  ];

  files.forEach(file => {
    const filePath = resolve(OUTPUT_DIR, file.name);
    writeFileSync(filePath, file.content, 'utf-8');
    console.log(`  ✅ ${file.name}`);
  });

  console.log(`\n✅ ${designSystemUpper} Foundation token generation complete!`);
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
  const designSystemArg = process.argv[2] as DesignSystem | undefined;
  
  // Validate design system argument
  const validSystems: DesignSystem[] = ['uswds', 'material', 'bootstrap'];
  
  if (designSystemArg && !validSystems.includes(designSystemArg)) {
    console.error(`\n❌ Error: Invalid design system "${designSystemArg}"\n`);
    console.error('Valid options: uswds, material, bootstrap\n');
    console.error('Usage:');
    console.error('  npm run generate:foundation           # Defaults to uswds');
    console.error('  npm run generate:foundation uswds');
    console.error('  npm run generate:foundation material');
    console.error('  npm run generate:foundation bootstrap\n');
    process.exit(1);
  }
  
  const designSystem = designSystemArg || 'uswds';
  
  generateFoundationTokens(designSystem).catch(error => {
    console.error('\n❌ Error generating foundation tokens:', error.message);
    process.exit(1);
  });
}
