/**
 * Generate CSS custom property files from parsed USWDS tokens
 * 
 * This generator creates the foundation layer CSS files that contain
 * all USWDS design tokens as CSS custom properties.
 * 
 * Architecture:
 *   USWDS Package → Token Parser → Foundation CSS → Semantic CSS → Components
 * 
 * Usage:
 *   npm run generate:css
 *   tsx src/lib/token-generators/generate-css-tokens.ts
 */

import { writeFileSync, mkdirSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import { parseUSWDSTokens } from './parse-tokens/uswds/index.js';
import type { ParsedTokens } from './types.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const OUTPUT_DIR = resolve(__dirname, '../../tokens/foundation/uswds');

/**
 * Generate color tokens CSS
 */
function generateColorCSS(tokens: ParsedTokens): string {
  const lines: string[] = [
    '/**',
    ' * USWDS Foundation Color Tokens',
    ' * ',
    ' * System colors, theme colors, and state colors from USWDS',
    ` * Generated from USWDS v${tokens.version}`,
    ' * ',
    ' * Do not edit this file directly. It is generated by:',
    ' * npm run generate:css',
    ' */',
    '',
    ':root {',
    '  /* ================================== */',
    '  /* System Colors                      */',
    '  /* ================================== */',
    ''
  ];

  // System colors
  Object.entries(tokens.colors.system).forEach(([name, token]) => {
    lines.push(`  --${name}: ${token.value};`);
  });

  lines.push('');
  lines.push('  /* ================================== */');
  lines.push('  /* Theme Colors                       */');
  lines.push('  /* ================================== */');
  lines.push('');

  // Theme colors
  Object.entries(tokens.colors.theme).forEach(([name, token]) => {
    const value = token.reference ? `var(--${token.value})` : token.value;
    lines.push(`  --${name}: ${value};`);
  });

  lines.push('');
  lines.push('  /* ================================== */');
  lines.push('  /* State Colors                       */');
  lines.push('  /* ================================== */');
  lines.push('');

  // State colors
  Object.entries(tokens.colors.state).forEach(([name, token]) => {
    const value = token.reference ? `var(--${token.value})` : token.value;
    lines.push(`  --${name}: ${value};`);
  });

  lines.push('}');
  lines.push('');

  return lines.join('\n');
}

