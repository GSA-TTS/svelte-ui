/**
 * Main generator function
 */
export function generateFoundationCSS() {
  console.log('=== USWDS Foundation CSS Generator ===\n');

  // Parse tokens
  console.log('Parsing USWDS tokens...');
  const tokens = parseUSWDSTokens();
  console.log(`  Parsed ${Object.keys(tokens.colors.system).length + Object.keys(tokens.colors.theme).length + Object.keys(tokens.colors.state).length} color tokens`);

  // Create output directory
  mkdirSync(OUTPUT_DIR, { recursive: true });
  console.log(`\nOutput directory: ${OUTPUT_DIR}`);

  // Generate CSS files
  console.log('\nGenerating CSS files...');

  const files = [
    { name: 'color.css', content: generateColorCSS(tokens) },
    { name: 'spacing.css', content: generateSpacingCSS(tokens) },
    { name: 'typesetting.css', content: generateTypesettingCSS(tokens) },
    { name: 'shadow.css', content: generateShadowCSS(tokens) },
    { name: 'opacity.css', content: generateOpacityCSS(tokens) },
    { name: 'z-index.css', content: generateZIndexCSS(tokens) },
    { name: 'flex.css', content: generateFlexCSS(tokens) },
    { name: 'order.css', content: generateOrderCSS(tokens) },
    { name: 'all.css', content: generateAllCSS(tokens) }
  ];

  files.forEach(file => {
    const filePath = resolve(OUTPUT_DIR, file.name);
    writeFileSync(filePath, file.content, 'utf-8');
    console.log(`  ✅ ${file.name}`);
  });

  console.log('\n✅ Foundation CSS generation complete!');
}

// Run if called directly
if (import.meta.url === `file://${process.argv[1]}`) {
  generateFoundationCSS();
}
