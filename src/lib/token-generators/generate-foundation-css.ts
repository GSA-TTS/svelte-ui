/**
 * Generate typesetting tokens CSS
 */
function generateTypesettingCSS(tokens: ParsedTokens): string {
  const lines: string[] = [
    '/**',
    ' * USWDS Foundation Typesetting Tokens',
    ' * ',
    ' * Font families, sizes, weights, line heights, letter spacing, and measure',
    ` * Generated from USWDS v${tokens.version}`,
    ' * ',
    ' * Do not edit this file directly. It is generated by:',
    ' * npm run generate:css',
    ' */',
    '',
    ':root {',
    '  /* ================================== */',
    '  /* Font Families                      */',
    '  /* ================================== */',
    ''
  ];

  // Font families
  Object.entries(tokens.typography.family).forEach(([name, token]) => {
    const fontStack = Array.isArray(token.value) 
      ? token.value.map(f => f.includes(' ') ? `"${f}"` : f).join(', ')
      : token.value;
    lines.push(`  --font-${name}: ${fontStack};`);
  });

  lines.push('');
  lines.push('  /* ================================== */');
  lines.push('  /* Font Sizes                         */');
  lines.push('  /* ================================== */');
  lines.push('');

  // Font sizes
  Object.entries(tokens.typography.size).forEach(([name, token]) => {
    lines.push(`  --font-size-${name}: ${token.value};`);
  });

  lines.push('');
  lines.push('  /* ================================== */');
  lines.push('  /* Font Weights                       */');
  lines.push('  /* ================================== */');
  lines.push('');

  // Font weights
  Object.entries(tokens.typography.weight).forEach(([name, token]) => {
    lines.push(`  --font-weight-${name}: ${token.value};`);
  });

  lines.push('');
  lines.push('  /* ================================== */');
  lines.push('  /* Line Heights                       */');
  lines.push('  /* ================================== */');
  lines.push('');

  // Line heights
  Object.entries(tokens.typography.lineHeight).forEach(([name, token]) => {
    lines.push(`  --line-height-${name}: ${token.value};`);
  });

  lines.push('');
  lines.push('  /* ================================== */');
  lines.push('  /* Letter Spacing                     */');
  lines.push('  /* ================================== */');
  lines.push('');

  // Letter spacing
  Object.entries(tokens.typography.letterSpacing).forEach(([name, token]) => {
    lines.push(`  --letter-spacing-${name}: ${token.value};`);
  });

  lines.push('');
  lines.push('  /* ================================== */');
  lines.push('  /* Measure (Line Length)              */');
  lines.push('  /* ================================== */');
  lines.push('');

  // Measure
  Object.entries(tokens.typography.measure).forEach(([name, token]) => {
    lines.push(`  --measure-${name}: ${token.value};`);
  });

  lines.push('}');
  lines.push('');

  return lines.join('\n');
}

